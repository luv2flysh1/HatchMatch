/**
 * Find the best matching image URL for a fly name
 * Returns null to fall back to type-based placeholder
 */
export function getFlyImageUrl(flyName: string): string | null {
  const normalized = flyName.toLowerCase().trim();

  // Check if we have a specific image for this fly
  if (FLY_IMAGES[normalized]) {
    return FLY_IMAGES[normalized];
  }

  // Try partial matches
  for (const [pattern, url] of Object.entries(FLY_IMAGES)) {
    if (normalized.includes(pattern) || pattern.includes(normalized)) {
      return url;
    }
  }

  return null;
}

/**
 * Get a placeholder image URL based on fly type
 * Uses placehold.co for reliable placeholder images with fly type labels
 */
export function getFlyTypePlaceholder(flyType: string): string {
  const type = flyType.toLowerCase();

  // Using placehold.co - a reliable placeholder service
  // Format: https://placehold.co/WIDTHxHEIGHT/BGCOLOR/TEXTCOLOR?text=TEXT
  const size = '200x200';

  switch (type) {
    case 'dry':
      return `https://placehold.co/${size}/8B4513/FFFFFF?text=Dry+Fly`;
    case 'nymph':
      return `https://placehold.co/${size}/2F4F4F/FFFFFF?text=Nymph`;
    case 'streamer':
      return `https://placehold.co/${size}/1B7FA8/FFFFFF?text=Streamer`;
    case 'emerger':
      return `https://placehold.co/${size}/6B8E23/FFFFFF?text=Emerger`;
    case 'wet':
      return `https://placehold.co/${size}/4682B4/FFFFFF?text=Wet+Fly`;
    case 'terrestrial':
      return `https://placehold.co/${size}/228B22/FFFFFF?text=Terrestrial`;
    default:
      return `https://placehold.co/${size}/666666/FFFFFF?text=Fly`;
  }
}
